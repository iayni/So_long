#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*cs1;
	unsigned char	*cs2;
	size_t			i;

	i = 0;
	cs1 = (unsigned char *)s1;
	cs2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	if (cs1 == cs2)
		return (0);
	while (cs1[i] == cs2[i] && i < (n - 1))
		i++;
	return (cs1[i] - cs2[i]);
}
